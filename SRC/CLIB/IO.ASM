	MODULE	lseek
;
; "io.h"
;---------------------------------------------------------------------
; Функция низко-уровневого ввода/вывода
;---------------------------------------------------------------------
; Функция устанавливает указатель в файле на новое место расположения,
; определяемое аргументами high и low, соответственно старшая и млад-
