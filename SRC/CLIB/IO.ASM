;       SEEK_CUR    от текущей позиции указателя в файле
;       SEEK_END    от конца файла
;
; Функция возвращает структуру f_point, определенную в "types.h"
; При ошибке возвращается -1 и errno содержит номер ошибки.
; Установка указателя за пределами файла, ошибки не вызывает.
; Для устройств, не приспособленных к поиску (таких как терминалы
; и принтеры), возвращается значение -1.
;
; Usage:
;
;	static f_point *p;	// или "f_point *p" вне функции
;
;	p = lseek(FD fd, p->high, p->low, type);
;	printf("value: %X %X\n", p->high, p->low);
;
;
; struct fpoint *lseek(FD fd, uint high, uint low, int type)
; struct fpoint *lseek(FD fd, uint high, uint low, char type)
;
lseek_::
	bit	7,l			; std... devices ?
	jr	nz,@1			; yes
	ld	a,l			; descriptor
	push	ix
	push	bc
	pop	ix			; low
	ld	hl,4
	add	hl,sp
	ld	b,(hl)			; type
	ex	de,hl			; hl=high
	ld	c,15h
	rst	10h
	ld	(high),hl
	ld	(low),ix
	ld	hl,low
	pop	ix
	ret	nc
	ld	(errno_##),a
@1:	ld	hl,-1
	ret

low:	dw	0
high:	dw	0

	ENDMODULE


