;
; size_t write(FD fd, void *buff, count)
;
write_::
	push	ix
	ld	a,l			; file descriptor
	ex	de,hl			; hl = buffer address
	ld	e,c			; de = number of bytes to write
	ld	d,b
	ld	c,14h
	rst	10h
	ex	de,hl			; hl = number of bytes actually write
	pop	ix
	ret	nc
	ld	(errno_##),a
	ld	hl,-1
	ret

	ENDMODULE


