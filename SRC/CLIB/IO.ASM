;---------------------------------------------------------------------
; creat()  Функция низко-уровневого ввода/вывода
;---------------------------------------------------------------------
; Функция creat создает новый файл.
; Если файл уже существует, урезает его до нулевой длины.
; Значение attr состоит из одной или нескольких констант:
; S_IWRITE и S_IREAD, объявленных в "stat.h". Если задаются обе
; константы, они объединяются при помощи логического ИЛИ "|".
; Функция возвращает номер дексриптора.
; При ошибке, возвращается значение -1 (0FFFFh) и errno
; содержит номер ошибки.
;
; uint creat(char *name, int attr)
; uint creat(char *name, char attr)
;--------------------------------------------------------------------------

; "io.h"
;---------------------------------------------------------------------
; open()  Функция низко-уровневого ввода/вывода
;---------------------------------------------------------------------
; Функция open открывает файл, определяемый по path-имени и подготавливает
; его к последующему чтению или записи, что определяется посредством
; oflag. Аргумент oflag является целым выражением, состоящим из комби-
; нации одной или более манифестных констант, объявленных в "io.h".
; Если задано больше одной константы, то они объединяются при помощи
; логического оператора ИЛИ "|". Значения аргумента oflag приводятся
; ниже:
;
;       O_RDWR       Файл открыт одновременно для чтения и записи.
;                    Если задается этот флаг, может быть выбран
;                    либо флаг O_RDONLY, либо O_WRONLY.
;
;       O_RDONLY     Файл открыт только для чтения. Если задается
;                    этот флаг, может быть выбран либо флаг O_RDWR,
;                    либо O_WRONLY.
;
;       O_WRONLY     Файл открыт только для записи. Если задан этот
;                    флаг, может быть задан также либо флаг O_RDONLY,
;                    либо ORDWR.
;
;       O_TRUNC      Существующий файл открыт на чтение и запись, и
;                    усечен к длине 0. Этот файл должен иметь разре-
;                    шение на запись. Содержимое файла уничтожается.
;
;       O_CREAT      Новый файл создан и открыт для чтения/записи;
;                    это не эффективно, если существует файл, опре-
;                    деляемый по path-имени.
;
;       O_EXCL       Возвращается значение ошибки, если существует
;                    файл, определяемый по path-имени. Применяется
;                    только вместе с O_CREATE.
;
;       O_APPEND     Указатель в файле переместить на конец файла.
;                    Для режимов O_RDONLY, O_TRUNC и O_CREAT не
;                    имеет смысла.
;
; Замечание!  O_TRUNC полностью уничтожает содержимое существующего
; файла, поэтому нужно внимательно использовать эту константу.
;
; Функция возвращает номер дескриптора файла, если Ok.
; При ошибке, возвращает значение -1 (0FFFFh) и ячейка errno содержит
; номер ошибки.
;
; in: hl = *pathname (указатель на имя файла)
;     de = oflag (тип операций)
; out: hl = номер дескриптора, если Ok
;      hl = -1 при ошибке
;
; uint open(char *pathname, int oflag)
; uint open(char *pathname, char oflag)
;
creat_::
open_::
	; e=oflag
	push	ix
	ld	a,3
	and	e			; mask 1,0 bits
	xor	3
	ld	d,a			; save "a"
	; a=0..2
	dec	a
	jr	z,open			; O_RDONLY
	ld	a,0Ch
	and	e			; 3,2 bits ?
	jr	z,open			; no  (O_WRONLY and O_RDWR)
	; O_CREAT or O_TRUNC
	ld	c,0Ah			; create file
	bit	4,e
	jr	z,trunc			; no O_EXCL
	inc	c			; create new file
trunc:	ld	a,20h			; attr "archive"
	rst	10h
	jr	op24
	;
open:	push	de
	ld	a,d			; O_RDONLY, O_WRONLY, O_RDWR
	ld	c,11h
	rst	10h
	pop	de
	jr	c,op24			; file not found
	bit	5,e			; O_APPEND
	jr	z,op24			; no
	; уст. указатель в конец файла
	push	af			; save file descriptor
	ld	hl,0
	push	hl
	pop	ix
	ld	b,2			; SEEK_END
	ld	c,15h			; move_fp
	rst	10h
	pop	bc
	ld	a,b
	;
op24:	ld	l,a			; file descriptor
	ld	h,0
	pop	ix
	ret	nc			; Ok
	ld	(errno_##),a
	ld	hl,-1			; error
	ret

