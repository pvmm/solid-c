; Если длина path-имени превышает len, то path-имя обрезается на длину
; len, при этом символ "\" в конец строки не ставится.
; Если аргумент pathbuff равен NULL, то getcwd вызывает malloc для
; выделения буфера размером len байтов. Этот выделенный буфер позже может
; быть освобожден посредством функции free.
; Функция возвращает указатель на pathbuff.
; При ошибке возвращается NULL и errno содержит номер ошибки.
;
; char *getcwd(char *pathbuff, int len)
;
getcwd_::
	;push	ix
	ld	(@6+1),de
	ld	a,l
	or	h
	jr	nz,@1
	ex	de,hl			; hl=len
	inc	hl
	call	malloc_##
	ld	a,l
	or	h
	ld	a,30			; ENOMEM (Not enough memory)
	jr	z,@4
	;
@1:	ld	(@5+1),hl
	ld	hl,4100h		; exe-header as temp buffer
	push	hl
	inc	hl
	inc	hl
	; get current directory
	ld	c,1Eh
	rst	10h
	jr	c,@3
	ex	de,hl			; hl=end string
	ld	a,"\"
	dec	hl
	dec	hl
	cp	(hl)
	jr	z,@2
	inc	hl
	ld	(hl),a
	inc	hl
	ld	(hl),0
	; get current disk
@2:	ld	c,2
	rst	10h
	pop	hl
	jr	c,@4
	ld	c,"A"
	add	a,c
	ld	(hl),a
	inc	hl
	ld	(hl),":"
	dec	hl			; hl=4100h
	;
@5:	ld	de,0
	push	de
@6:	ld	bc,1
	ldir
	xor	a
	ld	(de),a
	pop	hl
	;pop	ix
	ret
	;
@3:	pop	hl
@4:	;pop	ix
	ld	(errno_##),a
	ld	hl,0			; NULL
	ret

	ENDMODULE


