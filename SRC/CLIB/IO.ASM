; "io.h"
;---------------------------------------------------------------------
; Функция низко-уровневого ввода/вывода
;---------------------------------------------------------------------
; Функция читает число байт count из файла в буфер buff.
; Операция чтения начинается с текущей позиции указателя в файле.
; Функция возвращает число реально прочитанных байтов, которое
; однако, может быть меньше заявленного в count (например встре-
; тится конец файла). Мaксимaльнoе числo бaйтoв, кoтoрoе мoжнo
; прoчитaть из фaйлa зa oдин рaз, рaвнo 65534; пoскoльку 65535
; (OxFFFF) является неoтличимым oт -1, пoэтoму будет вoзврaщенa
; oшибкa.
; Функция возвращает 0, если делается попытка чтения конца файла.
; При ошибке чтения, возвращается -1 и errno содержит номер ошибки.
;
; size_t read(FD fd, void *buff, count)
;
read_::
	push	ix
	ld	a,l			; file descriptor
	ex	de,hl			; hl = buffer address
	ld	e,c			; de = number of bytes to read
	ld	d,b
	ld	c,13h
	rst	10h
	ex	de,hl			; hl = number of bytes actually read
	pop	ix
	ret	nc
	ld	(errno_##),a
	ld	hl,-1
	ret

	ENDMODULE


